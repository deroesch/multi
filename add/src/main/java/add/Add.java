package add;

/**
 * A simple class for adding long integers
 * 
 * @author doug.roesch@gmail.com
 */
public class Add {

	/**
	 * Returns the sum of two addends
	 * 
	 * @param a the left addend
	 * @param b the right addend
	 * @return the sum of a + b
	 */

	public long add(long a, long b) {
		return a + b;
	}

}
