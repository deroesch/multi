package mult;

/**
 * A simple class for multiplying long integers
 * 
 * @author doug.roesch@gmail.com
 */
public class Mult {

	/**
	 * Returns the product of two multiplicands
	 * 
	 * @param a the left multiplicand
	 * @param b the right multiplicand
	 * @return the product of a * b
	 */
	public long mult(long a, long b) {
		return a * b;
	}
}
